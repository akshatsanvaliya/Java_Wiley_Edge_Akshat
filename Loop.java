
public class Loop {

	public static void main(String[] args) {
		//nested for
for(int i=0; i< 10; i++) {
	for(int j=0; j< 10; j++)
	{
		System.out.println("Nested for: -" + j);
	}
	System.out.println("For: " + i);
}
//while loop to print 1-10
	int i = 0;
	while (i <= 9) {
	  System.out.println("While Loop: "+ i);
	  i++;
	}
	}
	
	

}

